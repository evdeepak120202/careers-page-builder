export function getUniqueLocations(jobs: Job[]): string[] {
  return Array.from(new Set(jobs.map((job) => job.location))).sort();
}

export function getUniqueWorkPolicies(jobs: Job[]): string[] {
  return Array.from(new Set(jobs.map((job) => job.work_policy))).sort();
}

export function getUniqueEmploymentTypes(jobs: Job[]): string[] {
  return Array.from(new Set(jobs.map((job) => job.employment_type))).sort();
}

export function filterJobs(
  jobs: Job[],
  filters: {
    search?: string;
    location?: string;
    workPolicy?: string;
    employmentType?: string;
    department?: string;
  }
): Job[] {
  return jobs.filter((job) => {
    if (filters.search) {
      const searchLower = filters.search.toLowerCase();
      if (
        !job.title.toLowerCase().includes(searchLower) &&
        !job.location.toLowerCase().includes(searchLower) &&
        !job.department.toLowerCase().includes(searchLower)
      ) {
        return false;
      }
    }
    if (filters.location && job.location !== filters.location) {
      return false;
    }
    if (filters.workPolicy && job.work_policy !== filters.workPolicy) {
      return false;
    }
    if (filters.employmentType && job.employment_type !== filters.employmentType) {
      return false;
    }
    if (filters.department && job.department !== filters.department) {
      return false;
    }
    return true;
  });
}

